let resultado = 1 + 10;

// Subtração (-) 
resultado = 5 - 3

// Multiplicação (*) 
resultado = 18 * 3

// Divisão (/)
 resultado = 10 / 2.5

// Módulo (%) - Pega o resto da divisão do primeiro valor pelo segundos.
